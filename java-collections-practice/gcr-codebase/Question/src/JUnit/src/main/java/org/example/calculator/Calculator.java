package org.example.calculator;

public class Calculator {
    public double add(double a, double b){
        return a+b;
    }
    public double subtract(double a , double b){
        return  a-b;
    }


    public int division(int a, int b){
        return a / b;
    }

    public double multiple(double a, double b){
        return a*b;
    }
}
